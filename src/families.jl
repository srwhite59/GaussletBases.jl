const _POSITIVE_COEFFICIENTS = Dict(
    :G4 => _G4_POSITIVE_COEFFICIENTS,
    :G6 => _G6_POSITIVE_COEFFICIENTS,
    :G8 => _G8_POSITIVE_COEFFICIENTS,
    :G10 => _G10_POSITIVE_COEFFICIENTS,
)

function _positive_family_coefficients(name::Symbol)
    haskey(_POSITIVE_COEFFICIENTS, name) ||
        throw(ArgumentError("unknown gausslet family $(name); expected one of $(collect(keys(_POSITIVE_COEFFICIENTS)))"))
    return copy(_POSITIVE_COEFFICIENTS[name])
end

function _full_family_coefficients(positive_coefficients::AbstractVector{<:Real})
    length(positive_coefficients) > 0 || throw(ArgumentError("family coefficient list cannot be empty"))
    # Recover the full symmetric coefficient list from the stored k >= 0 data.
    tail = reverse(positive_coefficients[2:end])
    return vcat(tail, collect(Float64, positive_coefficients))
end

"""
    GaussletFamily(name)

Canonical built-in gausslet family descriptor such as `GaussletFamily(:G10)`.
"""
struct GaussletFamily
    name::Symbol
    positive_coefficients::Vector{Float64}

    function GaussletFamily(name::Symbol)
        new(name, _positive_family_coefficients(name))
    end
end

function Base.show(io::IO, family::GaussletFamily)
    print(io, "GaussletFamily(", repr(family.name), ")")
end

"""
    Gausslet(family; center=0.0, spacing=1.0)

Callable one-dimensional gausslet defined exactly by a compact Gaussian stencil.
"""
struct Gausslet <: AbstractBasisFunction1D
    family::GaussletFamily
    center_value::Float64
    spacing::Float64

    function Gausslet(family::GaussletFamily, center::Real, spacing::Real)
        spacing_value = Float64(spacing)
        spacing_value > 0.0 || throw(ArgumentError("Gausslet requires spacing > 0"))
        new(family, Float64(center), spacing_value)
    end
end

Gausslet(family::Union{GaussletFamily, Symbol}; center::Real = 0.0, spacing::Real = 1.0) =
    Gausslet(family isa Symbol ? GaussletFamily(family) : family, center, spacing)

center(g::Gausslet) = g.center_value

function _gausslet_stencil_data(g::Gausslet)
    positive_coefficients = g.family.positive_coefficients
    coefficients_full = _full_family_coefficients(positive_coefficients)
    radius = length(positive_coefficients) - 1
    primitive_spacing = g.spacing / 3.0
    scale = 1.0 / sqrt(2.0 * pi * g.spacing)

    coefficients_scaled = Float64[scale * coefficient for coefficient in coefficients_full]
    primitive_list = AbstractPrimitiveFunction1D[
        Gaussian(center = g.center_value + primitive_spacing * offset, width = primitive_spacing)
        for offset in (-radius):radius
    ]

    return coefficients_scaled, primitive_list
end

function stencil(g::Gausslet)
    coefficients_scaled, primitive_list = _gausslet_stencil_data(g)
    return FunctionStencil(coefficients_scaled, primitive_list)
end

function derivative(g::Gausslet, x::Real; order::Int = 1)
    derivative_order = _check_nonnegative_order(order)
    derivative_order == 0 && return value(g, x)

    st = stencil(g)
    total = 0.0
    for i in eachindex(coefficients(st))
        total += coefficients(st)[i] * derivative(primitives(st)[i], x; order = derivative_order)
    end
    return total
end
